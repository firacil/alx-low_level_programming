#include <ctype.h>
#include <stdio.h>

/**
 * _isalpha - checks alphabetic charachters.
 * @c: test for charachters.
 * Return: 0 or 1.
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

/**
 * main - prints calling function.
 * Return: 0.
 */

int main(void)
{
	int r;

	r = _isalpha('H');
	putchar(r + '0');
	r = _isalpha('o');
	putchar(r + '0');
	r = _isalpha(108);
	putchar(r + '0');
	r = _isalpha(';');
	putchar(r + '0');
	putchar('\n');
	return (0);
}
