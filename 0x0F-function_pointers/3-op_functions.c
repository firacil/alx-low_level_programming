#include "3-calc.h"

/**
 * op_add - return sum of a & b.
 * @a: first number.
 * @b: second number.
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - return difference of a & b.
 * @a: first number.
 * @b: second number.
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - return product of a & b.
 * @a: first number.
 * @b: second number.
 * Return:product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - return sum of a & b.
 * @a: first number.
 * @b: second number.
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return reminder of a & b.
 * @a: first number.
 * @b: second number.
 * Return: reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
